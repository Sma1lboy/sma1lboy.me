const UNKNOWN_BLOCK: string[] = [
  "в–‘в–‘в–‘в–‘в–‘",
  "в–‘   в–‘",
  "в–‘ ? в–‘",
  "в–‘   в–‘",
  "в–‘в–‘в–‘в–‘в–‘",
];

const UNKNOWN_SHADOW: string[] = [
  "в–‘в–‘в–‘в–‘в–‘ ",
  "в–‘   в–‘ ",
  "в–‘ ? в–‘в–‘",
  "в–‘   в–‘в–‘",
  "в–‘в–‘в–‘в–‘в–‘в–‘",
  " в–‘в–‘в–‘в–‘в–‘",
];

const UNKNOWN_SLIM: string[] = [
  "в”Ңв”Җв”Җв”Җв”җ",
  "в”Ӯ   в”Ӯ",
  "в”Ӯ ? в”Ӯ",
  "в”Ӯ   в”Ӯ",
  "в””в”Җв”Җв”Җв”ҳ",
];

function getUnknown(style: FontStyle): string[] {
  switch (style) {
    case "shadow":
      return UNKNOWN_SHADOW;
    case "slim":
      return UNKNOWN_SLIM;
    default:
      return UNKNOWN_BLOCK;
  }
}

export function renderAscii(text: string, style: FontStyle): string {
  const font = FONTS[style];
  const input = text.toUpperCase();
  const unknown = getUnknown(style);

  const charArrays: string[][] = [];
  for (const ch of input) {
    charArrays.push(font.chars[ch] ?? unknown);
  }

  if (charArrays.length === 0) return "";

  const lines: string[] = [];
  for (let row = 0; row < font.height; row++) {
    const parts: string[] = [];
    for (const charArr of charArrays) {
      parts.push(charArr[row] ?? "");
    }
    lines.push(parts.join(" ".repeat(font.spacing)));
  }

  return lines.join("\n");
}
